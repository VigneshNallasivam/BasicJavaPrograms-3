package com.Java.basics;

public class PrintElements
{
	public static void main(String[] args) 
	{    
        int [] array = {1, 2, 3, 4, 5};  
        System.out.println("Elements of given array: ");    
        for (int i = 0; i < array.length; i++) 
        {  
            System.out.print(array[i] + " ");  
        }  
    }  
}
